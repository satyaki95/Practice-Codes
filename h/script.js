// Trainings Start
const trainings = document.getElementById("trainings");
const trainings2 = document.getElementsByClassName("trainings")[0];
trainings.addEventListener('mousemove', () => 
{trainings2.style.display = 'block'});

trainings2.addEventListener('mousemove', () =>
{trainings2.style.display = 'block'
trainings.classList.add('ab')
});

trainings.addEventListener('mouseleave', () => 
{trainings2.style.display = 'none'});

trainings2.addEventListener('mouseleave', () =>
{trainings2.style.display = 'none'
trainings.classList.remove('ab')});
// Trainings end

